/* ----- PUBLIC FUNCTIONS ----- */

/**
 * @brief Register a custom sprite.
 *
 * @param[in] name Name to assign to sprite.
 * @param[in] filename Path to sprite file relative to asset directory.
 * @param[in] numFrames If animated sprite, number of animation frames.
 * If not animated, use `1`.
 * @param[in] origX Horizontal origin (center) of sprite.
 * @param[in] origY Vertical origin (center) of sprite.
 *
 * @return Index of new sprite or ::AER_SPRITE_NULL if unsuccessful.
 *
 * @throw ::AER_NULL_ARG if argument `name` or argument `filename` is
 * `NULL`.
 * @throw ::AER_SEQ_BREAK if called outside sprite registration stage.
 * @throw ::AER_BAD_VAL if argument `numFrames` is less than `1` or argument
 * `name` is already in use by another sprite.
 * @throw ::AER_BAD_FILE if argument `filename` does not point to valid file.
 *
 * @since 1.0.0
 *
 * @sa AERSpriteReplace
 */
int32_t AERSpriteRegister(const char *name, const char *filename,
                          size_t numFrames, uint32_t origX, uint32_t origY);

/**
 * @brief Override a vanilla sprite with a custom sprite.
 *
 * @param[in] spriteIdx Index of vanilla sprite to override.
 * @param[in] filename Path to sprite file relative to asset directory.
 * @param[in] numFrames If animated sprite, number of animation frames.
 * If not animated, use `1`.
 * @param[in] origX Horizontal origin (center) of sprite.
 * @param[in] origY Vertical origin (center) of sprite.
 *
 * @throw ::AER_FAILED_LOOKUP if argument `spriteIdx` is an invalid sprite.
 * @throw ::AER_SEQ_BREAK if called outside sprite registration stage.
 * @throw ::AER_NULL_ARG if argument `filename` is `NULL`.
 * @throw ::AER_BAD_VAL if argument `numFrames` is less than `1`.
 *
 * @since 1.0.0
 *
 * @sa AERSpriteRegister
 */
void AERSpriteReplace(int32_t spriteIdx, const char *filename, size_t numFrames,
                      uint32_t origX, uint32_t origY);

/**
 * @brief Query the total number of vanilla and mod sprites registered.
 *
 * @return Number of sprites or `0` if unsuccessful.
 *
 * @throw ::AER_SEQ_BREAK if called before end of sprite registration stage.
 *
 * @since 1.0.0
 */
size_t AERSpriteGetNumRegistered(void);

/**
 * @brief Query the sprite with a specific name.
 *
 * @param[in] name Name of sprite.
 *
 * @return Sprite index or ::AER_SPRITE_NULL if unsuccessful.
 *
 * @throw ::AER_SEQ_BREAK if called before end of sprite registration stage.
 * @throw ::AER_NULL_ARG if argument `name` is `NULL`.
 * @throw ::AER_FAILED_LOOKUP if no sprite with name given by argument `name`.
 *
 * @since 1.3.0
 */
int32_t AERSpriteGetByName(const char *name);

/**
 * @brief Query the name of a sprite.
 *
 * @param[in] spriteIdx Sprite of interest.
 *
 * @return Name of sprite or `NULL` if unsuccessful.
 *
 * @throw ::AER_SEQ_BREAK if called before end of sprite registration stage.
 * @throw ::AER_FAILED_LOOKUP if argument `spriteIdx` is an invalid sprite.
 *
 * @since 1.0.0
 */
const char *AERSpriteGetName(int32_t spriteIdx);

